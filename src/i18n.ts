/**
 * Set the active language.  Call once per render cycle with hass.language.
 * Falls back to "en" for unsupported languages.
 */
export function setLanguage(lang: string | undefined): void {
  _lang = lang && translations[lang] ? lang : "en";
}

/**
 * Look up a translation key.  Returns the English fallback when the key
 * is missing in the active language.
 */
export function t(key: string): string {
  return translations[_lang]?.[key] ?? translations.en?.[key] ?? key;
}
